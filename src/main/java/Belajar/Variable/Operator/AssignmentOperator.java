package Belajar.Variable.Operator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("hasil x+ ="+(a+=5));
        System.out.println("hasil x+ ="+(a-=3));
        System.out.println("hasil x+ ="+(a*=2));
        System.out.println("hasil x+ ="+(a/=3));
        System.out.println("hasil x+ ="+(a%=2));
    }
}
